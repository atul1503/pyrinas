def main() -> int:
    x: int = 10
    y: int = 5
    
    if x > y:
        print("x is greater than y")
    else:
        print("x is not greater than y")
        
    if x == 10:
        print("x is 10")
    
    if y == 10:
        print("y is 10")
    else:
        print("y is not 10")
    
    return 0